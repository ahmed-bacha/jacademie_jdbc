(ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
NAME VARCHAR(25) NOT NULL,
NUMERO INTEGER NOT NULL,
EQUIPE_ID INTEGER NOT NULL,
PRIMARY KEY (ID));

