package org.jacademie.domain;

public class CommandeStatus {

	private Integer id;
	
	private String label;
	
	public CommandeStatus() {
		super();
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}
	
	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}
}
